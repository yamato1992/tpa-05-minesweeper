  data: function() {
    return {
      tiles: new Array(ROW_SIZE).fill(new Array(COLUMN_SIZE)),
    };
  },
  methods: {
    startGame: function() {
      this.initializeTiles();
    },
    initializeTiles: function() {
      this.tiles = new Array(ROW_SIZE).fill(new Array(COLUMN_SIZE));
    },
  },
  components: {
    Tile,
  }
};
</script>

